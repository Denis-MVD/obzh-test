if st.session_state.state == "login":
    st.title("🎖️ ТРЕНАЖЕР НВП РК")
    name = st.text_input("Введите ФИО:")
    c1, c2 = st.columns(2)
    with c1: 
        if st.button("10 КЛАСС"): st.session_state.cl = "10 класс"
    with c2:
        if st.button("11 КЛАСС"): st.session_state.cl = "11 класс"

    if 'cl' in st.session_state:
        st.write(f"Выбран: {st.session_state.cl}")
        for t in DATABASE[st.session_state.cl].keys():
            if st.button(t, use_container_width=True):
                if name:
                    st.session_state.u_name, st.session_state.u_th = name, t
                    st.session_state.start, st.session_state.ans = datetime.now(), {}
                    st.session_state.duration = 25
                    raw = DATABASE[st.session_state.cl][t]
                    sh = []
                    for q, o, c in random.sample(raw, len(raw)):
                        sh.append((q, random.sample(o, len(o)), c))
                    st.session_state.qs, st.session_state.state = sh, "test"
                    st.rerun()
                else: st.warning("Введите имя!")

elif st.session_state.state == "test":
    st_autorefresh(1000, key="timer")
    rem = timedelta(minutes=st.session_state.duration) - (datetime.now() - st.session_state.start)
    sec = max(0, int(rem.total_seconds()))
    st.markdown(f'<div class="sticky-timer">⏱️ {sec//60:02d}:{sec%60:02d}</div>', unsafe_allow_html=True)
    
    if sec <= 0: st.session_state.state = "fin"; st.rerun()

    for i, (q, o, c) in enumerate(st.session_state.qs):
        st.write(f"**{i+1}. {q}**")
        st.session_state.ans[i] = st.radio(f"q{i}", o, key=f"r{i}_{len(st.session_state.qs)}", label_visibility="collapsed")

    if st.button("ЗАВЕРШИТЬ"): st.session_state.state = "fin"; st.rerun()

elif st.session_state.state == "fin":
    score = sum(1 for i, (q, o, c) in enumerate(st.session_state.qs) if st.session_state.ans.get(i) == c)
    total = len(st.session_state.qs)
    grade = "5" if score/total >= 0.9 else "4" if score/total >= 0.75 else "3" if score/total >= 0.5 else "2"
    
    st.title(f"Ваш результат: {score}/{total}")
    st.subheader(f"Оценка: {grade}")

    wr = [(q, o, c) for i, (q, o, c) in enumerate(st.session_state.qs) if st.session_state.ans.get(i) != c]

    if wr:
        st.error("Разбор ошибок:")
        for q, o, c in wr:
            with st.expander(f"Вопрос: {q}"): st.success(f"Верный ответ: {c}")
        
        if st.button("🔥 ПОВТОРИТЬ ОШИБКИ (15 МИН)"):
            st.session_state.qs = wr
            st.session_state.ans, st.session_state.start = {}, datetime.now()
            st.session_state.duration = 15
            st.session_state.state = "test"
            st.rerun()
    else: st.balloons()

    if st.button("В МЕНЮ"): 
        for k in list(st.session_state.keys()): del st.session_state[k]
        st.rerun()
